package supply.medium.home.bean;

/**
 *
 * @author Intel
 */
public class CompanyAdvertisementBean {
private String advertisementKey;
private String companyKey;
private String userKey;
private String hoverText;
private String linkPage;
private String imagePath;

    public String getAdvertisementKey() {
        return advertisementKey;
    }

    public void setAdvertisementKey(String advertisementKey) {
        this.advertisementKey = advertisementKey;
    }

    public String getCompanyKey() {
        return companyKey;
    }

    public void setCompanyKey(String companyKey) {
        this.companyKey = companyKey;
    }

    public String getUserKey() {
        return userKey;
    }

    public void setUserKey(String userKey) {
        this.userKey = userKey;
    }

    public String getHoverText() {
        return hoverText;
    }

    public void setHoverText(String hoverText) {
        this.hoverText = hoverText;
    }

    public String getLinkPage() {
        return linkPage;
    }

    public void setLinkPage(String linkPage) {
        this.linkPage = linkPage;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }



}
