package supply.medium.home.bean;

/**
 *
 * @author Intel
 */
public class CompanyWebsiteBean {
    
private String companyWebsiteKey;
private String companyKey;
private String urlName;
private String websiteUrl;

    public String getCompanyWebsiteKey() {
        return companyWebsiteKey;
    }

    public void setCompanyWebsiteKey(String companyWebsiteKey) {
        this.companyWebsiteKey = companyWebsiteKey;
    }

    public String getCompanyKey() {
        return companyKey;
    }

    public void setCompanyKey(String companyKey) {
        this.companyKey = companyKey;
    }

    public String getUrlName() {
        return urlName;
    }

    public void setUrlName(String urlName) {
        this.urlName = urlName;
    }

    public String getWebsiteUrl() {
        return websiteUrl;
    }

    public void setWebsiteUrl(String websiteUrl) {
        this.websiteUrl = websiteUrl;
    }


    
}
