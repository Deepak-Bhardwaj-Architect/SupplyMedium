public class PasswordStore
{
	/*
	 * Method : getEncryptedValue()
	 * 
	 * Input : password string
	 * 
	 * Return : encrypted password
	 * 
	 * Purpose: It is used to convert password to encrypted password
	 */
	
	public String getEncryptedValue( String password )
	{
		return password;
	}

	/*
	 * Method : getDecryptedValue()
	 * 
	 * Input : encrypted password
	 * 
	 * Return : password string
	 * 
	 * Purpose: It is used to convert encrypted password to password
	 */
	
	public String getDecryptedValue( String password )
	{
		return password;
	}

}
