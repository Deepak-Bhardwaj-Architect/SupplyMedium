public class MarketingPersonBean {

    private String marketing_person_key;
    private String date_of_joining;
    private String marketing_person_name;
    private String address_details;
    private String account_details;
    private String contact_number;
    private String e_mail;
    private String pass_word;
    private String user_status;

    public String getDate_of_joining() {
        return date_of_joining;
    }

    public void setDate_of_joining(String date_of_joining) {
        this.date_of_joining = date_of_joining;
    }

    public String getMarketing_person_key() {
        return marketing_person_key;
    }

    public void setMarketing_person_key(String marketing_person_key) {
        this.marketing_person_key = marketing_person_key;
    }

    public String getMarketing_person_name() {
        return marketing_person_name;
    }

    public void setMarketing_person_name(String marketing_person_name) {
        this.marketing_person_name = marketing_person_name;
    }

    public String getAddress_details() {
        return address_details;
    }

    public void setAddress_details(String address_details) {
        this.address_details = address_details;
    }

    public String getAccount_details() {
        return account_details;
    }

    public void setAccount_details(String account_details) {
        this.account_details = account_details;
    }

    public String getContact_number() {
        return contact_number;
    }

    public void setContact_number(String contact_number) {
        this.contact_number = contact_number;
    }

    public String getE_mail() {
        return e_mail;
    }

    public void setE_mail(String e_mail) {
        this.e_mail = e_mail;
    }

    public String getPass_word() {
        return pass_word;
    }

    public void setPass_word(String pass_word) {
        this.pass_word = pass_word;
    }

    public String getUser_status() {
        return user_status;
    }

    public void setUser_status(String user_status) {
        this.user_status = user_status;
    }

}
